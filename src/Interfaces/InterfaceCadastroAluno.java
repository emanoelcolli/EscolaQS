    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        ViaCEP viaCep = new ViaCEP();
        
        try {
            viaCep.buscar(jtfCEP.getText());
            jtfCidade.setText(viaCep.getLocalidade());
            jtfUF.setText(viaCep.getUf());
        } catch (ViaCEPException ex) {
            Logger.getLogger(InterfaceCadastroAluno.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "CEP Invalido.");
            jtfCEP.setText("");
            jtfCidade.setText("");
            jtfUF.setText("");
    }//GEN-LAST:event_jbBuscarActionPerformed

}
    
