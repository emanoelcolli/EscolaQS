    private void jbCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCadastrarActionPerformed
        Aluno aluno = null;
        try {
            aluno = new Aluno(Integer.parseInt(jtfMatricula.getText()), jtfNome.getText(), jtfCPF.getText(), jtfEmail.getText(), jtfCEP.getText(), jtfCidade.getText(), jtfUF.getText());
        } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(null, "Campos Inválidos!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Campos Inválidos!");
        }
        AlunoDAO dao = new AlunoDAO();
        if (i==0) {
            if (dao.cadastra(aluno)) {
            JOptionPane.showMessageDialog(null, "Aluno Cadastrado com sucesso!"); 
            limpaCampos();
            }
        }
        else{
            try{
                boolean atualizarAluno = dao.atualizarAluno(Integer.parseInt(jtfMatricula.getText()), jtfNome.getText(), jtfCPF.getText(), jtfEmail.getText(), jtfCEP.getText(), jtfCidade.getText(), jtfUF.getText());
            } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(null, "Campos Inválidos!");
            } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Campos Inválidos!");
            }
            this.dispose();
        }
    }//GEN-LAST:event_jbCadastrarActionPerformed

